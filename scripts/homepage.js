/*For the side bar show and cancel button */
hidesidebar = () =>{
    var sidebar = document.getElementById("sidebar");
    sidebar.style.display="none";
   // document.body.style.position="";
}
  
showsidebar = () =>{
    var sidebar = document.getElementById("sidebar");
    sidebar.style.display = "block";
   // document.body.style.position="fixed";
}